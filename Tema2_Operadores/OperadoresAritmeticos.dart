void main() {

  //  Suma
  int resultadoSuma = 5 + 3;
  print('El resultado de la suma 5 + 3 es igual a: $resultadoSuma');
  //  Resta
  int resultadoResta = 10 - 4;
  print('El resultado de la resta 10 - 4 es igual a: $resultadoResta');
  //  Multiplicacion
  int resultadoMulti = 6 * 2;
  print('El resultado de la multiplicacion 6 * 2 es igual a: $resultadoMulti');
  //  Division
  double resultadoDiv = 10 / 2;
  print('El resultado de la division 10 / 2 es igual a: $resultadoDiv');
  //  Modulo - Residuo
  int resultadoModulo = 10 % 3;
  print('El resultado del modulo 10 % 3 es igual a: $resultadoModulo');
}