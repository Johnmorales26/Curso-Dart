void main() {

  //  Operador de incremento - Incrementa el valor de una variable en una unidad
  int contador = 0;
  contador++;
  //  Operador de decremento - Decrementa el valor de una variable en una unidad
  contador--;
  //  Pre-Incremento
  contador = 5;
  int resultado = ++contador;
  print(resultado);
  //  Post-Incremento
  contador = 5;
  int resultadoPost = contador++;
  print(resultadoPost);
  

}