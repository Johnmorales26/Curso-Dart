void main() {
  bool condicion1 = true;
  bool condicion2 = false;

//  Uso de && logico
  bool resultado = condicion1 && condicion2;
  print(resultado);

//  Uso de || logico
  bool resultadoOLogico = condicion1 || condicion2;
  print(resultadoOLogico);

//  Uso de ! logico
  bool condicion = false;
  bool resultadoNoLogico = !condicion;
  print(resultadoNoLogico);
}
