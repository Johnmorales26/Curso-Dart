void main() {

  //  Operador igual a
  bool esIgual = (5 == 6);
  print(esIgual);
  //  Operador no igual a
  bool esDiferente = (5 != 5);
  print(esDiferente);
  //  Operador menor que
  bool esMenor = (6 < 5);
  print(esMenor);
  //  Operador mayor que
  bool esMayor = (2 > 3);
  print(esMayor);
  //  Operador menor o igual que
  bool esMenorIgual = (6 <= 5);
  print(esMenorIgual);
  //  Operador mayor o igual que
  bool esMayorIgual = (3 >= 5);
  print(esMayorIgual);
}